export const loader: Loader = async ({ params }) => {
  const data = await graphql.request<PostBySlugQuery, PostBySlugQueryVariables>(
    PostBySlugDocument,
    { slug: params.slug },
    { "x-hasura-admin-secret": process.env.HASURA_ADMIN_SECRET as string }
  );

  if (data.posts_connection.edges.length === 0) {
    return json({ notFound: true }, { status: 404 });
  }

  const [post] = data.posts_connection.edges;

  return json(post, {
    headers: {
      "cache-control": "max-age=86400",
    },
  });
};

type Post = PostBySlugQuery["posts_connection"]["edges"][number];
type MaybePost = Post & { notFound?: boolean };

export const meta: MetaFunction = (route) => {
  const data = route.data as MaybePost;
  if (data.notFound) {
    return {
      title: "404",
      description:
        "Oops! we looked everywhere but couldn't find what you were looking for, you probably followed a broken link.",
    };
  }

  return {
    title: data.node.title,
    description: data.node.excerpt,
  };
};

